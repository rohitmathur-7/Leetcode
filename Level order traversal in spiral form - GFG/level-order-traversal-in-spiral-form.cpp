/* A binary tree node has data, pointer to left child
   and a pointer to right child  
struct Node
{
    int data;
    struct Node* left;
    struct Node* right;
    
    Node(int x){
        data = x;
        left = right = NULL;
    }
}; */
int height(Node* root){
    if(root==NULL) return 0;
    int l=1+height(root->left);
    int r=1+height(root->right);
    return max(l,r);
}
void solve(Node* root,int level,vector<int>& ans,int c){
    if(root==NULL) return;
    if(level==1){
        ans.push_back(root->data);
    }
    else{
        if(c%2!=0){
            solve(root->left,level-1,ans,c);
            solve(root->right,level-1,ans,c);
        }
        else{
            solve(root->right,level-1,ans,c);
            solve(root->left,level-1,ans,c);
        }
    }
}

//Function to return a list containing the level order traversal in spiral form.
vector<int> findSpiral(Node *root)
{
    int h=height(root);
    vector<int> ans;
    int c=0;
    for(int i=1;i<=h;i++){
        solve(root,i,ans,c);
        c++;
    }
    return ans;
}
